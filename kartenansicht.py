def finde_spielerposition(position):
    y = 0
    muster = re.compile(fr"{position}")
    ergebnisse = muster.finditer(spielkarte)
    for ergebnis in ergebnisse:
        x = ergebnis.span()
        y = x[0]
        print(ersetze_ort_durch_x(kopie, y))
        break


def ersetze_ort_durch_x(text, index=0, replacement='X'):
    return '%s%s%s' % (text[:index], replacement, text[index + 1:])
